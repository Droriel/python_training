class PersonalData:

    def __init__(self, firstname, middlename, lastname, nickname, title, company, address):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address


class PhoneNumbers:

    def __init__(self, home, mobile, work, fax):
        self.home = home
        self.mobile = mobile
        self.work = work
        self.fax = fax


class Emails:

    def __init__(self, email1, email2, email3):
        self.email1 = email1
        self.email2 = email2
        self.email3 = email3


class Wwww:

    def __init__(self, www):
        self.www = www


class AdditionalData:

    def __init__(self, address, phone):
        self.address = address
        self.phone = phone


class Notes:

    def __init__(self, notes):
        self.notes = notes



